/* Дафна инвестировала $100 под простые 10%. Другими словами, ежегодно
*  инвестиция должна приносить 10% инвестированной суммы, т.е. $10 каждый год:
*  прибыль = 0,10 х исходный баланс
*  В то же время Клео инвестировала $100 под сложные 5%. Это значит, что прибыль
*  составит 5% от текущего баланса, включая предыдущую накопленную прибыль:
*  прибыль = 0,05 х текущий баланс Клео зарабатывает 5% от $100 в первый год,
*  что дает ей $105. На следующий год она зарабатывает 5% от $105,
*  что составляет $5.25, и т.д. Напишите программу, которая вычислит, сколько
*  лет понадобится для того, чтобы сумма баланса Клео превысила сумму баланса Дафны,
*  с отображением значений обоих балансов за каждый год.
*/
#include <iostream>
const float daphne = 100;
const float cleo = 100;
using namespace std;

int main() {
    float daphneRes = daphne;
    float cleoRes = cleo;

    int year = 0;
    while (daphneRes >= cleoRes)
    {
        year++;
        daphneRes += daphne * 0.10;
        cleoRes += cleoRes * 0.05;

        cout << "The year " << year << " profit is\n"
            << "$" << daphneRes << " for Daphne\n"
            << "$" << cleoRes << " for Cleo\n" << endl;
    }

    cout << "It would take " << year << " years, to Cleo's balance got bigger then Daphne's";
    return 0;
}